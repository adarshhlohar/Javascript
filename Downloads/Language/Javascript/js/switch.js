let age = 45;
switch (age) {
    case 45:
        console.log("youre age is 45");
        break;
    case 54:
        console.log("youre age is 54");
        break;
    case 65:
        console.log("youre age is 65");
        break;
    default:
        console.log("youre age is invalid");
}